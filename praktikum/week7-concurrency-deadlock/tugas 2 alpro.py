angka = float(input("Masukkan sebuah bilangan: "))
# Meminta input bilangan dari pengguna.

if angka > 0:
    print("Bilangan Positif")
    # Kondisi angka lebih dari 0 → positif.
elif angka < 0:
    print("Bilangan Negatif")
    # Kondisi angka kurang dari 0 → negatif.
else:
    print("Bilangan Nol")
    # Jika tidak positif dan tidak negatif → nol.
