nilai = int(input("Masukkan nilai mahasiswa: "))


if nilai >= 88 and nilai <= 100:
    print("Nilai A")
elif nilai >= 70 and nilai <= 87:
    print("Nilai B") 
elif nilai >= 60 and nilai <= 69:
    print("Nilai C")
elif nilai >= 40 and nilai <= 59:
    print("Nilai D")
else:
    print("Nilai E")
    
